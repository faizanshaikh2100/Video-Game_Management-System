    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        customerName = jTextField5.getText();

        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","yedtutti");
            Statement st = con.createStatement();
            ResultSet rs= st.executeQuery("SELECT * FROM customer WHERE customerName ='"+customerName+"'");
            
                

            if(rs.next()){
                 customerType=rs.getString(2);
                mobNo=rs.getInt(3);
                age =rs.getInt(4);
                gender=rs.getString(5);
                amountRemaining=rs.getInt(6);

                //adding them to fields
                
                jTextField1.setText(mobNo+"");
                jTextField2.setText(age+"");
                jTextField3.setText(amountRemaining+"");
                jComboBox2.setSelectedItem(customerType);
                jComboBox3.setSelectedItem(gender);
                
                
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

